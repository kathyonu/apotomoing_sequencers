  def de_comma
	foo = self.strip.downcase
	  foo.gsub!(/,\s?/, " ")            # commas : replaces every comma followed by a space, or not, with one space 
      foo.gsub!(/\s\s+/, " ")           # spaces : replaces multiple spacing with one space
  end

